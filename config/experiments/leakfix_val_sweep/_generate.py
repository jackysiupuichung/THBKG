#!/usr/bin/env python3
"""Generate val-window sweep configs for the leakfix p3_eahgt_both setting.

Sweeps val_min_year == val_max_year ∈ {2013, 2014, 2015}, three seeds each.
All other settings inherit from p3_eahgt_both_leakfix.yaml semantics:
    - leak fix applied via shared build_context_graph (no per-config knob)
    - 40 epochs, no early stopping (we report final-epoch + best-by-val)
    - lambdarank loss, allrank impl, ndcg_k=50
    - HPs identical to leaked v2 (so the only deltas are leak fix + val window)

Run: python config/experiments/leakfix_val_sweep/_generate.py
Writes 9 yaml files + 9 sbatch scripts.
"""
from pathlib import Path

REPO = Path(__file__).resolve().parents[3]
CONFIG_DIR = Path(__file__).resolve().parent
SCRIPTS_DIR = REPO / "scripts" / "advancement_prediction" / "leakfix_val_sweep"
SCRIPTS_DIR.mkdir(parents=True, exist_ok=True)

YEARS = [2013, 2014, 2015]
SEEDS = [0, 1, 7]

OUTPUT_ROOT = "/gpfs/scratch/bty414/opentarget_evidences/23.06/runs/leakfix_val_sweep"

CONFIG_TMPL = """\
# Auto-generated by leakfix_val_sweep/_generate.py — do not edit by hand.
# Leakfix variant of p3_eahgt_both with global temporal validation per
# Petrov et al. (RecSys 2025): val window is a single year just before
# the 2016 test cutoff. Three val years × three seeds sweep, no ES.
experiment:
  name: p3_eahgt_both_leakfix_v{year}_s{seed}

data:
  graph_file: /gpfs/scratch/bty414/opentarget_evidences/23.06/graph/hetero_graph_with_features_datatype.pt
  mappings_file: /gpfs/scratch/bty414/opentarget_evidences/23.06/progression/temporal_graph_datatype_mappings.pt
  undirected: true
  train_cutoff_year: 2010
  val_min_year: {year}
  val_max_year: {year}

model:
  name: hgt
  hidden_dim: 128
  num_heads: 2
  num_layers: 2
  dropout: 0.2
  use_rte: false
  use_edge_features: true
  edge_feat_cols: [0, 1]
  edge_feat_dim: 2

train:
  output_dir: {output_root}/v{year}_s{seed}
  num_epochs: 40
  lr: 0.0004346878320299101
  weight_decay: 0.0018260646941480677
  eta_min: 1.0e-6
  cosine_t_max: 10
  batch_size: 512
  num_neighbors: [20, 10]
  lambdarank:
    impl: allrank
    weighing_scheme: lambdaRank_scheme
    sigma: 1.4319388789983414
    ndcg_k: 50
  early_stopping:
    enabled: false
    patience: 5
    metric: ndcg_ta_mean@30
    mode: max

seed: {seed}
"""

SBATCH_TMPL = """\
#!/bin/bash
#SBATCH -J lf_v{year}_s{seed}
#SBATCH -o %x.o%j
#SBATCH -p gpushort
#SBATCH -n 8
#SBATCH --cpus-per-gpu=8
#SBATCH -t 1:0:0
#SBATCH --mem-per-cpu=11G
#SBATCH --gres=gpu:nvidia_a100_80gb_pcie:1

set -euo pipefail

# Hardcode the repo root. Slurm copies this script to
# /var/spool/slurmd/jobNNN/slurm_script before executing, so BASH_SOURCE
# and SLURM_SUBMIT_DIR don't reliably point at the original location.
REPO_ROOT="/data/home/bty414/opentarget_temporal_study/src/opentarget_het_graph"
cd "$REPO_ROOT"

source .venv/bin/activate
export WANDB_MODE="disabled"

# Per-epoch top-100 test snapshot so we can verify disease-concentration
# behaviour is independent of val-window choice.
export SAVE_PER_EPOCH_TOPK=100

python src/train_advancement_lambdarank.py \\
    --config config/experiments/leakfix_val_sweep/v{year}_s{seed}.yaml
"""

SWEEP_TMPL = r"""#!/bin/bash
# Submit all 9 leakfix val-window sweep runs.
set -euo pipefail
cd "$(dirname "${BASH_SOURCE[0]}")"
for f in run_v*.sh; do
    sbatch "$f"
done
"""


def main():
    n_cfg = 0
    n_sbatch = 0
    for year in YEARS:
        for seed in SEEDS:
            cfg_path = CONFIG_DIR / f"v{year}_s{seed}.yaml"
            cfg_path.write_text(CONFIG_TMPL.format(year=year, seed=seed, output_root=OUTPUT_ROOT))
            n_cfg += 1
            sbatch_path = SCRIPTS_DIR / f"run_v{year}_s{seed}.sh"
            sbatch_path.write_text(SBATCH_TMPL.format(year=year, seed=seed))
            sbatch_path.chmod(0o755)
            n_sbatch += 1
    sweep_path = SCRIPTS_DIR / "submit_all.sh"
    sweep_path.write_text(SWEEP_TMPL)
    sweep_path.chmod(0o755)
    print(f"Wrote {n_cfg} configs to {CONFIG_DIR}")
    print(f"Wrote {n_sbatch} sbatch scripts + submit_all.sh to {SCRIPTS_DIR}")


if __name__ == "__main__":
    main()
