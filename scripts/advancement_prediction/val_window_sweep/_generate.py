#!/usr/bin/env python3
"""Generate config files and Slurm scripts for the val-window sweep.

Five val-window variants × three seeds = 15 jobs. Train cutoff stays at
2010, test is ≥2016, only val_min/max_year and seed vary.

Variants:
  V1  2011-2015   (current baseline - default split)
  V2  2013-2015   (medium-narrow)
  V3  2014-2015   (narrow-near)
  V4  2015        (narrow-adjacent — the most test-adjacent)
  V5  2012-2013   (negative control — narrow but NOT adjacent to test)

Seeds: 42, 17, 123.
"""
from __future__ import annotations

from pathlib import Path
from textwrap import dedent

REPO_ROOT     = Path(__file__).resolve().parents[3]
CONFIG_DIR    = REPO_ROOT / "config" / "experiments" / "val_window_sweep"
SCRIPT_DIR    = REPO_ROOT / "scripts" / "advancement_prediction" / "val_window_sweep"
RUNS_BASE     = "/gpfs/scratch/bty414/opentarget_evidences/23.06/runs/val_window_sweep"

VARIANTS = [
    # (slug, val_min, val_max, comment)
    ("v1_2011_15", 2011, 2015, "baseline: current default split"),
    ("v2_2013_15", 2013, 2015, "medium-narrow window"),
    ("v3_2014_15", 2014, 2015, "narrow-near test"),
    ("v4_2015",    2015, 2015, "narrow-adjacent (most test-adjacent)"),
    ("v5_2012_13", 2012, 2013, "negative control: narrow but NOT adjacent"),
]
SEEDS = [42, 17, 123]

CONFIG_TEMPLATE = """\
# Auto-generated by val_window_sweep/_generate.py — do not edit by hand.
# {comment}
seed: {seed}

experiment:
  name: {name}

data:
  graph_file: /gpfs/scratch/bty414/opentarget_evidences/23.06/graph/hetero_graph_with_features_datatype.pt
  mappings_file: /gpfs/scratch/bty414/opentarget_evidences/23.06/progression/temporal_graph_datatype_mappings.pt
  undirected: true
  train_cutoff_year: 2010
  val_min_year: {val_min}
  val_max_year: {val_max}

model:
  name: hgt
  hidden_dim: 128
  num_heads: 2
  num_layers: 2
  dropout: 0.2
  use_rte: false
  use_edge_features: true
  edge_feat_cols: [0, 1]
  edge_feat_dim: 2

train:
  output_dir: {output_dir}
  num_epochs: 30
  lr: 0.0004346878320299101
  weight_decay: 0.0018260646941480677
  eta_min: 1.0e-6
  cosine_t_max: 10
  batch_size: 512
  num_neighbors: [20, 10]
  lambdarank:
    impl: allrank
    weighing_scheme: lambdaRank_scheme
    sigma: 1.4319388789983414
    ndcg_k: 50
  early_stopping:
    enabled: false   # disabled — we want full per-epoch trajectories
    patience: 5
    metric: ndcg@50
"""

SLURM_TEMPLATE = """\
#!/bin/bash
#SBATCH -J val_{slug}_s{seed}
#SBATCH -o %x.o%j
#SBATCH -p sae
#SBATCH -A pilot_sae_gpu
#SBATCH -n 8
#SBATCH --cpus-per-gpu=8
#SBATCH -t 4:0:0
#SBATCH --mem-per-cpu=11G
#SBATCH --gres=gpu:nvidia_a100_80gb_pcie:1

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${{BASH_SOURCE[0]}}")" && pwd)"
REPO_ROOT="${{SLURM_SUBMIT_DIR:-$(dirname "$(dirname "$(dirname "$SCRIPT_DIR")")")}}"
cd "$REPO_ROOT"

source .venv/bin/activate
export WANDB_MODE="disabled"

python src/train_advancement_lambdarank.py \\
  --config config/experiments/val_window_sweep/{slug}_s{seed}.yaml
"""

ORCHESTRATOR_TEMPLATE = """\
#!/bin/bash
# Submit the val-window experiment: 5 val-window variants × 3 seeds = 15 jobs.
# Each variant retrains p3_eahgt_both with a different val_min/max_year (train
# cutoff and test window held constant). The goal is to find whether a
# narrower, more test-adjacent val window makes the val_ndcg/rr signal
# predictive of test_rr (Spearman ρ > 0.5, selection regret < 1.0).

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${{BASH_SOURCE[0]}}")" && pwd)"
REPO_ROOT="$(dirname "$(dirname "$SCRIPT_DIR")")"
cd "$REPO_ROOT"

SWEEP_DIR="$SCRIPT_DIR/val_window_sweep"

JOBS=({jobs_quoted})

for job in "${{JOBS[@]}}"; do
    echo "Submitting $job"
    sbatch "$SWEEP_DIR/$job"
done

echo "All ${{#JOBS[@]}} jobs submitted. Use 'squeue -u $USER' to monitor."
"""

def main():
    CONFIG_DIR.mkdir(parents=True, exist_ok=True)
    SCRIPT_DIR.mkdir(parents=True, exist_ok=True)
    job_filenames = []
    for slug, vmin, vmax, comment in VARIANTS:
        for seed in SEEDS:
            name = f"p3_val_{slug}_s{seed}"
            output_dir = f"{RUNS_BASE}/{slug}_s{seed}"
            cfg_text = CONFIG_TEMPLATE.format(
                comment=comment, seed=seed, name=name,
                val_min=vmin, val_max=vmax, output_dir=output_dir,
            )
            cfg_path = CONFIG_DIR / f"{slug}_s{seed}.yaml"
            cfg_path.write_text(cfg_text)

            slurm_text = SLURM_TEMPLATE.format(slug=slug, seed=seed)
            slurm_path = SCRIPT_DIR / f"run_{slug}_s{seed}.sh"
            slurm_path.write_text(slurm_text)
            slurm_path.chmod(0o755)
            job_filenames.append(slurm_path.name)
    # orchestrator
    jobs_quoted = " ".join(f'"{j}"' for j in job_filenames)
    orch_path = REPO_ROOT / "scripts" / "advancement_prediction" / "run_val_window_sweep.sh"
    orch_path.write_text(ORCHESTRATOR_TEMPLATE.format(jobs_quoted=jobs_quoted))
    orch_path.chmod(0o755)
    print(f"Wrote {len(VARIANTS) * len(SEEDS)} configs to {CONFIG_DIR}")
    print(f"Wrote {len(VARIANTS) * len(SEEDS)} Slurm scripts to {SCRIPT_DIR}")
    print(f"Wrote orchestrator to {orch_path}")


if __name__ == "__main__":
    main()
